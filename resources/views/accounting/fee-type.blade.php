<script>
// Reset form when opening modal for new fee type
function resetForm() {
    document.getElementById('feeTypeForm').reset();
    document.getElementById('feeTypeForm').action = "{{ route('accounting.fee-type.store') }}";
    document.getElementById('methodField').innerHTML = '';
    document.getElementById('feeTypeModalLabel').innerHTML = `
        <span class="material-symbols-outlined" style="font-size: 20px; color: white !important;">payments</span>
        <span style="color: white !important;">Add New Fee Type</span>
    `;
    document.querySelector('.fee-type-submit-btn').innerHTML = `
        <span class="material-symbols-outlined" style="font-size: 16px; vertical-align: middle;">save</span>
        Save Fee Type
    `;
}

// Edit fee type function
function editFeeType(id) {
    fetch(`{{ url('/accounting/fee-type') }}/${id}`)
        .then(response => response.json())
        .then(data => {
            document.getElementById('feeTypeForm').action = '{{ route('accounting.fee-type.update', ':id') }}'.replace(':id', id);
            document.getElementById('methodField').innerHTML = '@method("PUT")';
            document.getElementById('feeTypeModalLabel').innerHTML = `
                <span class="material-symbols-outlined" style="font-size: 20px; color: white !important;">edit</span>
                <span style="color: white !important;">Edit Fee Type</span>
            `;
            document.querySelector('.fee-type-submit-btn').innerHTML = `
                <span class="material-symbols-outlined" style="font-size: 16px; vertical-align: middle;">save</span>
                Update Fee Type
            `;
            
            document.getElementById('fee_name').value = data.fee_name || '';
            document.getElementById('campus').value = data.campus || '';
            
            new bootstrap.Modal(document.getElementById('feeTypeModal')).show();
        })
        .catch(error => {
            console.error('Error:', error);
            alert('Error loading fee type data');
        });
}

// Search functionality
function performSearch() {
    const searchInput = document.getElementById('searchInput');
    const searchValue = searchInput.value.trim();
    const url = new URL(window.location.href);
    
    if (searchValue) {
        url.searchParams.set('search', searchValue);
    } else {
        url.searchParams.delete('search');
    }
    
    url.searchParams.set('page', '1');
    
    searchInput.disabled = true;
    const searchBtn = document.querySelector('.search-btn');
    if (searchBtn) {
        searchBtn.disabled = true;
        searchBtn.innerHTML = '<span class="spinner-border spinner-border-sm" role="status"></span>';
    }
    
    window.location.href = url.toString();
}

function handleSearchKeyPress(event) {
    if (event.key === 'Enter') {
        event.preventDefault();
        performSearch();
    }
}

function handleSearchInput(event) {
    // Auto-clear if input is empty
}

function clearSearch() {
    const url = new URL(window.location.href);
    url.searchParams.delete('search');
    url.searchParams.set('page', '1');
    
    const searchInput = document.getElementById('searchInput');
    searchInput.disabled = true;
    
    window.location.href = url.toString();
}

// Update entries per page
function updateEntriesPerPage(value) {
    const url = new URL(window.location.href);
    url.searchParams.set('per_page', value);
    url.searchParams.set('page', '1');
    window.location.href = url.toString();
}

// Print table
function printTable() {
    const printContents = document.querySelector('.default-table-area').innerHTML;
    const originalContents = document.body.innerHTML;
    
    document.body.innerHTML = `
        <div style="padding: 20px;">
            <h3 style="text-align: center; margin-bottom: 20px; color: #003471;">Fee Types List</h3>
            ${printContents}
        </div>
    `;
    
    window.print();
    document.body.innerHTML = originalContents;
    window.location.reload();
